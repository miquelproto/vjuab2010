#include "CParent.h"


//----------------------------------------------------------------------------
// Init data
//----------------------------------------------------------------------------
bool 
CProcess::Init(){
  m_bIsOk = true;

  if (!m_bIsOk){
    Release();
  }

  return m_bIsOk;
}

//----------------------------------------------------------------------------
// Finalize data
//----------------------------------------------------------------------------
void
CProcess::Done(){
  if (IsOk())
  {
    Release();
    m_bIsOk = false;
  }
}

//----------------------------------------------------------------------------
// Free memory
//----------------------------------------------------------------------------
void
CProcess::Release(){
//free memory
}


